    <p>
        Debian is regarded as a stability-first OS, and is very customizable.
        It’s used as the base of many other different linux distros.
        You can choose your desktop environment with Debian.
        As part of the stability-first approach, releases are much less frequent.
        Debian’s installer with a built-in install guide can be found <a href="https://www.debian.org/" target="_blank">here</a>.
    </p>
    <h2>Linux Mint</h2>
    <p>
        Linux Mint is built off of Ubuntu, but has a more polished interface, perfect for people with experience using Windows.
        It uses the Cinnamon Desktop.
        You can also get Linux Mint Debian Edition, which is based off of Debian instead.
        Mint’s install guide can be found <a href="https://linuxmint-installation-guide.readthedocs.io/en/latest/" target="_blank">here</a>.
    </p>
    <h2>Pop!_OS</h2>
    <p>
