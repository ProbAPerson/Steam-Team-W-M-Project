        It’s preferred by gamers, but also used by developers and power users.
        More information can be found <a href="https://pop.system76.com/" target="_blank">here</a>.
    </p>
    <h2>Fedora Linux</h2>
    <p>
        Fedora is a distro backed by Red Hat, and is primarily an enterprise-focused distro.
        It focuses on newer technologies for developers.
        It uses Wayland as its desktop.
        More information about Fedora’s workstation OS can be found <a href="https://fedoraproject.org/workstation/" target="_blank">here</a>.
    </p>
    <h2>Arch</h2>
    <p>
        Arch is a distro generally regarded as being for advanced Linux users who want full and total control of their machine.
        As a new install, Arch only comes with the basics, requiring the user to install their window manager or desktop environment of their choice.
        Arch’s install guide can be found <a href="https://wiki.archlinux.org/title/Installation_guide" target="_blank">here</a>.
